a=100

# This is totally a if_elif_else ladder
if (a>50):
    print("Greater than 50")
elif (a>76):
    print("Greater than 76")
elif (a>89):
    print("Greater than 89")
else:
    print("Not Greater")

# This total from if to else is called a ladder
