# In list we have to use the []
# Mutable= Can be changed
numb =[1,4,6,3,8,7]
print(numb)
numb[2]=100
# Here value will be changed
print(numb)


# In tuple we have to use ()
# Immutable= Cannot be changed
numb =(1,4,6,3,8,7)
# numb[2]=100
# This Cannot be changed as its the tuple
print("\n\n",numb)

